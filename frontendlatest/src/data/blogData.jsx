// Functions to help with blog operations
export const getBlogById = (id) => {
  return blogPosts.find((post) => post.id === id);
};

export const getRelatedPosts = (currentPostId, tagToMatch, count = 2) => {
  // Find posts that share tags with the current post but aren't the current post
  const currentPost = getBlogById(currentPostId);
  if (!currentPost) return [];

  const relatedPosts = blogPosts
    .filter((post) => {
      return (
        post.id !== currentPostId &&
        (tagToMatch
          ? post.tags.includes(tagToMatch)
          : post.tags.some((tag) => currentPost.tags.includes(tag)))
      );
    })
    .slice(0, count);

  return relatedPosts;
};

export const getRecentPosts = (count = 3, excludeId = null) => {
  return blogPosts
    .filter((post) => !excludeId || post.id !== excludeId)
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count);
};

export const getAllTags = () => {
  const allTags = new Set();
  blogPosts.forEach((post) => {
    post.tags.forEach((tag) => allTags.add(tag));
  });
  return Array.from(allTags);
};

export const getPostsByTag = (tag) => {
  return blogPosts.filter((post) => post.tags.includes(tag));
};

export const formatDate = (dateString) => {
  const options = { year: "numeric", month: "long", day: "numeric" };
  return new Date(dateString).toLocaleDateString("en-US", options);
};
